async function main() {
  console.log('Adding new figures (skipping existing)...');
  let added = 0;
  let skipped = 0;

  for (const figure of FIGURES) {
    const exists = await prisma.rabbi.findFirst({ where: { name: figure.name } });
    if (exists) {
      console.log(`⏭  ${figure.name}`);
      skipped++;
    } else {
      await prisma.rabbi.create({ data: figure });
      console.log(`✓  ${figure.name}`);
      added++;
    }
  }

  console.log(`\nDone. Added ${added}, skipped ${skipped}.`);
}

main()
  .catch((e) => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
